x = 10 + 3 * 2
print(x)
# exponentiation 2 ** 3
# multiplication or division
# addition or subtraction
# this is the order of operations

x = 10 + 3 * 2 ** 2
print(x)
# 2 ** 2 = 4
# 3 * 4 = 12
# 10 + 12 = 22

x = (10 + 3)  * 2 ** 2
print(x)
# parenthesis (10 + 3)
# exponentiation 2 ** 3
# multiplication or division
# addition or subtraction
# this is the order of operations

